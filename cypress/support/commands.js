Cypress.Commands.add('inputText', (elm, text) => { 
    cy.get(elm).type(text)
})

Cypress.Commands.add('logText', (elm) => { 
    cy.get(elm).invoke('val').then(($value) => {
        cy.log('ELEMENT TEXT: ' + $value)
    })
})